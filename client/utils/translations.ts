export const formatBanglaDate = (date: Date): string => {
  const options: Intl.DateTimeFormatOptions = {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    weekday: 'long'
  };
  
  const dateStr = date.toLocaleDateString('bn-BD', options);
  return dateStr;
};

export const formatBanglaNumber = (num: number): string => {
  const bnDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
  return num.toString().replace(/\d/g, digit => bnDigits[parseInt(digit)]);
};

